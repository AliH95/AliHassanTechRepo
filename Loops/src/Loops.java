
public class Loops {

	public static void main(String[] args) {
		
		int a;
		a=1;
		while (a<=20) {
			System.out.println("Hello");
			//or System.out.print("Hello"); If you want to print in one line
			
			a=a+1;
		}
		
		//int A;
		for (int A=1; A<=10; A=A+1) {
			System.out.println(A);
		}

	}

}
